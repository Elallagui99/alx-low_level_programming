#include "3-calc.h"

/**
 * op_add - sum of 2 int
 * @a: 1st int
 * @b: 2nd int
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub of 2 int
 * @a: 1st int
 * @b: 2nd int
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplie 2 int
 * @a: 1st int
 * @b: 2nd int
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - devide 2 int
 * @a: 1st int
 * @b: 2nd int
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo of 2 int
 * @a: 1st int
 * @b: 2nd int
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
